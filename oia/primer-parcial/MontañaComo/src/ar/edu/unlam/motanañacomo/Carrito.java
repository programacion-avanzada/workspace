package ar.edu.unlam.motanañacomo;

public class Carrito {
	
	private int x;
	private int y;
	
	public Carrito(int x) {
		this.x = x;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
